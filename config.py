"""
config.py — Конфигурация для индексации романа "Отцы и дети".

Все настраиваемые параметры находятся здесь: модель эмбеддингов,
размеры чанков, пути к векторному хранилищу, количество результатов поиска,
настройки LLM и параметры пайплайна Corrective RAG.

Каждое значение может быть переопределено переменной окружения с тем же именем,
что упрощает конфигурирование при запуске в Docker или разных окружениях.

LLM бэкенд: LM Studio (OpenAI-совместимый API) на localhost:1234.
            При запуске в Docker переопределите LLM_BASE_URL.
Эмбеддинги: sentence-transformers/paraphrase-multilingual-mpnet-base-v2
            (запускаются в процессе, не требуют отдельного сервера).
"""

import os


def _bool(name: str, default: bool) -> bool:
    """Читает булево значение из переменной окружения.

    Принимает 'true', '1', 'yes' (без учёта регистра) как True.
    """
    val = os.environ.get(name)
    if val is None:
        return default
    return val.strip().lower() in ("true", "1", "yes")


# ---------------------------------------------------------------------------
# LLM — LM Studio предоставляет OpenAI-совместимый REST API
# Установите LLM_MODEL в название модели из вкладки "Model" в LM Studio.
# В Docker переопределите LLM_BASE_URL на адрес контейнера Ollama.
# ---------------------------------------------------------------------------
LLM_BASE_URL: str = os.environ.get(
    "LLM_BASE_URL",
    "http://localhost:11434/v1"
)

LLM_API_KEY: str = os.environ.get(
    "LLM_API_KEY",
    "lm-studio"  # LM Studio не требует реального ключа, но поле должно быть заполнено
)

LLM_MODEL: str = os.environ.get(
    "LLM_MODEL",
    "qwen2.5:3b"
)

LLM_TEMPERATURE: float = float(os.environ.get(
    "LLM_TEMPERATURE",
    "0.0"  # Нулевая температура для детерминированных ответов
))

# Модели DeepSeek R1 / QwQ генерируют цепочку размышлений внутри тегов
# <think>...</think> перед финальным ответом. Qwen2.5 не использует такие теги,
# поэтому по умолчанию выключено. Включите только для DeepSeek R1 / QwQ.
LLM_STRIP_THINKING_TAGS: bool = _bool("LLM_STRIP_THINKING_TAGS", False)

# ---------------------------------------------------------------------------
# Эмбеддинги — Мультиязычная модель, оптимизированная для русского языка
# ---------------------------------------------------------------------------
EMBEDDING_MODEL: str = os.environ.get(
    "EMBEDDING_MODEL",
    "sentence-transformers/paraphrase-multilingual-mpnet-base-v2"
)

# ---------------------------------------------------------------------------
# Разбиение на чанки
# ---------------------------------------------------------------------------
CHUNK_SIZE: int = int(os.environ.get(
    "CHUNK_SIZE",
    "1500"  # Увеличенный размер для литературного текста (сохраняет контекст абзацев)
))

CHUNK_OVERLAP: int = int(os.environ.get(
    "CHUNK_OVERLAP",
    "300"  # Перекрытие 20% от CHUNK_SIZE для плавных переходов между чанками
))

# ---------------------------------------------------------------------------
# Векторное хранилище — ChromaDB в процессе (без отдельного сервера)
# ---------------------------------------------------------------------------
CHROMA_DB_PATH: str = os.environ.get(
    "CHROMA_DB_PATH",
    "./book_chroma_db_ru"
)

COLLECTION_NAME: str = os.environ.get(
    "COLLECTION_NAME",
    "fathers_and_sons_ru"
)

# ---------------------------------------------------------------------------
# Поиск
# ---------------------------------------------------------------------------
TOP_K: int = int(os.environ.get(
    "TOP_K",
    "5"  # Количество возвращаемых чанков при поиске
))

# ---------------------------------------------------------------------------
# Пакетная обработка
# ---------------------------------------------------------------------------
UPSERT_BATCH_SIZE: int = int(os.environ.get(
    "UPSERT_BATCH_SIZE",
    "100"  # Размер пакета при добавлении чанков в ChromaDB
))

# ---------------------------------------------------------------------------
# Поддерживаемые типы файлов для индексации
# ---------------------------------------------------------------------------
SUPPORTED_EXTENSIONS: set[str] = {".txt"}

# ---------------------------------------------------------------------------
# Corrective RAG — ограничения на количество повторных попыток
# ---------------------------------------------------------------------------
MAX_RETRIEVE_RETRIES: int = int(os.environ.get(
    "MAX_RETRIEVE_RETRIES",
    "2"  # Максимальное число повторных поисков при отсутствии релевантных чанков
))

MAX_GENERATE_RETRIES: int = int(os.environ.get(
    "MAX_GENERATE_RETRIES",
    "1"  # Максимальное число повторных генераций при обнаружении галлюцинаций
))