"""
prompts.py — Шаблоны запросов для пайплайна Corrective RAG.

Шаблоны (используют LangChain ChatPromptTemplate):
    - QUERY_REWRITE_PROMPT    : Переписать запрос пользователя для улучшения векторного поиска
    - CHUNK_GRADING_PROMPT    : Оценить релевантность чанка запросу (да/нет)
    - GENERATION_PROMPT       : Сгенерировать обоснованный ответ на основе контекста
    - HALLUCINATION_PROMPT    : Проверить, что ответ подтверждается контекстом (да/нет)
    - SUMMARIZATION_PROMPT    : Кратко изложить содержимое документа или набора чанков

Особенности:
    - Шаблоны рассчитаны на модели, следующие инструкциям (Saiga, Qwen, DeepSeek и др.)
    - Запросы на оценку и проверку галлюцинаций требуют структурированного бинарного
      вывода (JSON), чтобы избежать неоднозначных свободных ответов
    - Теги размышлений (<think>...</think>) от рассуждающих моделей удаляются
      в graph.py с помощью вспомогательной функции strip_thinking_tags()
    - Язык шаблонов: русский. Ожидается, что модель отвечает на том же языке,
      что и вопрос пользователя
    - При цитировании используется формат: (источник: <имя_файла>, глава: <название_главы>)
"""

from __future__ import annotations

import re

from langchain_core.prompts import ChatPromptTemplate


# ---------------------------------------------------------------------------
# Вспомогательная функция: удаление тегов размышлений моделей DeepSeek R1 / QwQ
# ---------------------------------------------------------------------------

def strip_thinking_tags(text: str) -> str:
    """Удаляет блоки <think>...</think>, генерируемые рассуждающими моделями.

    Удаляет весь блок вместе с окружающими пробельными символами,
    так что вызывающая сторона получает только итоговый ответ.

    Аргументы:
        text: Необработанный вывод LLM, потенциально содержащий блоки <think>.

    Возвращает:
        Очищенный текст с удалёнными секциями <think>...</think>.
    """
    cleaned = re.sub(r"<think>.*?</think>", "", text, flags=re.DOTALL)
    return cleaned.strip()


